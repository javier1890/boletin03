package boletin03;

public class Seguimiento01 {

	public static void main(String[] args) {
		// 6. Calcular el valor de la posición fibonacci usando recursividad.
				int suma;
			    for (int i = 1; i < 4; i++) {
					for (int j = 3; j >0; j--) {
						suma=i*10+j;
						
						System.out.println("La suma es:"+suma);
					}
				 }
				}

	}


